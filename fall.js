// darkgreen box

let darkgreenBox = document.querySelector(".box1");

function turnDarkgreen() {

    document.body.style.background = "#4E801F";
    document.body.style.color = "#4E801F";
}

darkgreenBox.addEventListener("click", turnDarkgreen);


